package global.dados;

public class Presente {
	
	private int idpresente;
	private String referencia;
	private Double valor;
	private Double metaAtingida;
	private int idevento;
	private int qtd;
	private String convidado;
	private String mensagem;
	private String foto;	
	
	public String getFoto() {
		return foto;
	}
	public void setFoto(String foto) {
		this.foto = foto;
	}
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	public Double getMetaAtingida() {
		return metaAtingida;
	}
	public void setMetaAtingida(Double metaAtingida) {
		this.metaAtingida = metaAtingida;
	}
	public int getIdpresente() {
		return idpresente;
	}
	public void setIdpresente(int idpresente) {
		this.idpresente = idpresente;
	}
	public String getReferencia() {
		return referencia;
	}
	public void setReferencia(String referencia) {
		this.referencia = referencia;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getIdevento() {
		return idevento;
	}
	public void setIdevento(int idevento) {
		this.idevento = idevento;
	}
	public String getConvidado() {
		return convidado;
	}
	public void setConvidado(String convidado) {
		this.convidado = convidado;
	}
	public String getMensagem() {
		return mensagem;
	}
	public void setMensagem(String mensagem) {
		this.mensagem = mensagem;
	}
	
	
	
	

}
