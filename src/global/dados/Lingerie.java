package global.dados;

public class Lingerie {
	
	private int idlingerie;
	private int idevento;
	private String idproduto;
	private String referencia;
	private String descricao;
	private String cor;
	private String tamanho;
	private Double preco_tabela;
	private Double meta;
	private String marca;
	private String link;
	private String categoria;
	
	
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public Double getMeta() {
		return meta;
	}
	public void setMeta(Double meta) {
		this.meta = meta;
	}
	public int getIdlingerie() {
		return idlingerie;
	}
	public void setIdlingerie(int idlingerie) {
		this.idlingerie = idlingerie;
	}
	public int getIdevento() {
		return idevento;
	}
	public void setIdevento(int idevento) {
		this.idevento = idevento;
	}
	public String getIdproduto() {
		return idproduto;
	}
	public void setIdproduto(String idproduto) {
		this.idproduto = idproduto;
	}
	public String getReferencia() {
		return referencia;
	}
	public void setReferencia(String referencia) {
		this.referencia = referencia;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public Double getPreco_tabela() {
		return preco_tabela;
	}
	public void setPreco_tabela(Double preco_tabela) {
		this.preco_tabela = preco_tabela;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getTamanho() {
		return tamanho;
	}
	public void setTamanho(String tamanho) {
		this.tamanho = tamanho;
	}
	
}
